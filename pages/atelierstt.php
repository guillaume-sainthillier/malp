<h1>Des ateliers en ligne</h1>
<br />
<br />
<h2>Un atelier bimensuel de groupe</h2>
<p>
	Les principes de cet atelier sur internet sont  les mêmes qu'un atelier présentiel :<br /> propositions, écriture, partage, échanges.<br/>
	Les inscrit-e-s constituent un groupe virtuel (6 à 8 personnes)<br/>
	L'animatrice fait des propositions 2 fois par mois sur le site dans l'espace réservé à l'atelier, qui mettent en mouvement vers l'écriture. Les participant-e-s ont une semaine pour écrire le ou les textes, qu'ils partagent avec le reste du groupe. La semaine suivante permet à l'animatrice et au reste du groupe de faire des retours, jusqu'à la proposition suivante.<br/> 
	Les retours restent toujours bienveillants. Ce peut être des remarques, des questions, des choses qui plaisent, qui intriguent....<br/>
	L'animatrice a un droit de regard sur les retours des participant-e-s dans le soucis d'éviter des dérives, des propos blessants, inadaptés et de garantir un cadre d’échanges qui reste dans la stricte limite de l’écriture (par exemple, tout commentaire d’ordre religieux, politique ou raciste est proscrit).<br/>
	L'avantage d'un atelier en ligne est de disposer d’une semaine entière pour écrire son texte. Il devient ainsi possible de reprendre son premier jet, de corriger, de couper, de laisser murir... Ce temps de maturation d'une semaine est également bénéfique à la qualité des retours et des échanges.
</p>
<br />
<br />
<h2>Les règles de fonctionnement</h2>
<h3>Anonymat</h3>
<p>
	Lors de leur inscription, les participants choisissent le nom sous lequel ils seront connus dans l’atelier (ce peut être leur prénom ou un pseudo), ainsi que l’adresse e-mail qu’ils utiliseront. 
	Seuls les animateurs/trices savent à qui correspondent les pseudonymes. Ils ou elles s’engagent à ne pas divulguer les renseignements personnels concernant les participants, sans l’accord de ceux-ci.<br/>
</p>
<br />
<br />
<h3>Nétiquette</h3>
<p>
	Les participants s’engagent à respecter la nétiquette. Il est notamment interdit d’utiliser les adresses e-mails auxquels ils auraient accès pour faire de l’envoi en masse (spamming).<br/>
</p>
<br />
<br />
<h3>Confidentialité</h3>
<p>
	Les participants sont amenés à lire les textes écrits par les autres participants. 
	Chaque participant s’engage à ne pas faire usage des textes à l’extérieur sans l’accord express de leur auteur.
</p>
<br />
<br />
<h3>Format des textes</h3>
<p>
	Les textes seront de taille raisonnable, c’est-à-dire n’excédant pas trois feuillets standard (un feuillet standard comporte environ deux cent cinquante mots, soit mille cinq cents signes)<br/>
	Le non-respect de ces règles élémentaires peut conduire à la radiation de la liste de l’atelier. 
</p>
