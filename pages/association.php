<h3>Résumé</h3>
<p>
	Au départ cette intuition, cette idée qui germe et prend de plus en plus de place. Et si animer des ateliers d'écriture était « ma part du colibri », une contribution modeste mais engagée au monde ?
	Et si une des fonctions de l'atelier d'écriture était de résister ? Un atelier d'écriture comme acte de résistance?
	L'enjeu de l'exercice étant d'analyser une question à travers ses pratiques, se sont imposés à moi les deux sens du mot résistance, les deux versants.
	D'un côté quelles sont les résistances au sens d'entraves qui oeuvrent dans un atelier, dans mes ateliers ? Le rapport à la littérature, la question de l'art et du « bien écrit » , une langue prise au piège, l'intime comme frein et le corps : est-ce qu'il empêche ? Est-ce qu'il permet ?
	D'un autre côté est-ce que l'atelier fabrique, met en mouvement des résistances, au sens de capacités d'opposition à une autorité, une pensée dominante ? Est-ce que dans un atelier on parvient à s'affranchir, s'émanciper de quelque chose ? Écrire est-ce résister ? Est-ce s'affranchir ? De soi ? D'une certaine réalité? Y'a-t-il un affranchissement particulier à l'enfance ? À la vieillesse ? Est-ce qu'un atelier ne serait pas un espace de bifurcations ?
	L'écriture, ici littéraire, mais qui peut être chorégraphique, musicale, audiovisuelle... à mettre ou ne pas mettre entre toutes les mains ? Parce que l'écriture est un outil, une arme, un véritable enjeu politique, une fonction démocratique fondamentale. Parce je défends l'idée que tout le monde est capable d'écrire, de créer, de résister.
</p>
